package com.intel.director.swift.api;
/**
*
* @author Aakash
*/
public class SwiftContainer {

	public int count;
	public long bytes;
	public String name;
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public long getBytes() {
		return bytes;
	}
	public void setBytes(long bytes) {
		this.bytes = bytes;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
